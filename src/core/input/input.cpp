KEY_STATE Input::GetKeyState(int _key)
{
    if((_key >= 0) && (_key <= 349))
        return keyboard_map[_key];
    
    return KEY_STATE_RELEASED;
}

MOUSE_BUTTON_STATE Input::GetMouseButtonState(int _button)
{
    if((_button >= 0) && (_button <= 7))
        return mouse_map[_button];
    
    return MOUSE_BUTTON_STATE_RELEASED;
}

void Input::ChangeKeyState(int _key, KEY_STATE _state)
{
    if((_key >= 0) && (_key <= 349))
        keyboard_map[_key] = _state;
}

void Input::ChangeMouseState(MOUSE_BUTTON _button, MOUSE_BUTTON_STATE _state)
{
    if(_button >= 0 && _button <= 7)
        mouse_map[_button] = _state;
}

